/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */
	MPU_ConfigTypeDef myMpuConfig;
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_I2C1_Init();
  MX_I2C3_Init();
  MX_TIM4_Init();
  MX_TIM5_Init();
  MX_USART1_UART_Init();
  MX_USB_OTG_FS_PCD_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  /* USER CODE BEGIN 2 */

  //1. Initialize the MPU6050 module and I2C1
  MPU6050_Init(&hi2c1);
  //2. Configure Accel and Gyro parameters
  myMpuConfig.Accel_Full_Scale = AFS_SEL_4g;
  myMpuConfig.ClockSource = Internal_8MHz;
  myMpuConfig.CONFIG_DLPF = DLPF_184A_188G_Hz;
  myMpuConfig.Gyro_Full_Scale = FS_SEL_500;
  myMpuConfig.Sleep_Mode_Bit = 0;  //1: sleep mode, 0: normal mode
  MPU6050_Config(&myMpuConfig);
  HAL_Delay(50); // gyro needs ~30 ms to stabilize after power-on
  MPU6050_CompFilter_Init(&gCompFilter, 0.995f);

  // Initialize WS2812 LED strip with TIM5 and set pixel color
  WS2812_Init(&htim5);
  WS2812_SetPixel(30, 30, 30);

  // Start UART reception in interrupt mode for 1 byte
  HAL_UART_Receive_IT(&huart1, &uart_rx_byte, 1);

  // Initialize Motors
  TB6612_Init(&gMotorA);  // the other motor (left)
  TB6612_Init(&gMotorB);  // motor with encoder (right)

  TB6612_Brake(&gMotorA);
  TB6612_Brake(&gMotorB);

  // Initialize Encoder
  //1. Initialize MT6701 with I2C3
  MT6701_Init(&encoder, &hi2c3);
  MT6701_SetZero(&encoder);
  //2. Initialize Timer 3 for Encoder
  HAL_TIM_Base_Start_IT(&htim3);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  // TODO
	  uint32_t now = HAL_GetTick(); // Get the current system tick in milliseconds

	  // Toggle the user LED every 100 ms for a blinking effect
	  if (now - last_led_tick >= 100)
	  {
		  last_led_tick = now;
		  HAL_GPIO_TogglePin(USER_LED_GPIO_Port, USER_LED_Pin);
	  }

	  // Read IMU and run complementary filter at 100 Hz (10 ms)
	  if (now - last_imu_tick >= 10)
	  {
		  float dt = (now - last_imu_tick) * 0.001f;
		  last_imu_tick = now;
		  MPU6050_CompFilter_Update(&gCompFilter, dt);
	  }

	  // Send telemetry data every 100 ms
	  if (now - last_telemetry_tick >= 100)
	  {
		  last_telemetry_tick = now;

	      float speed = encoder.velocity_deg_s;  /* encoder-derived wheel speed */
	      float roll  = gCompFilter.roll;  /* side angle */
	      int16_t pitch = gCompFilter.pitch;  /* front angle */
	      int16_t counter = gTMP;  /* counter */

	      int len = snprintf(gTelemetry, sizeof(gTelemetry),
	                         "$%.2f,%.2f,%d,%d\r\n",
	                         speed, roll, pitch, counter);
	      HAL_UART_Transmit_IT(&huart1, (uint8_t *)gTelemetry, len);

	      gTMP = (gTMP + 1) % 20;
	  }

      if (gUART_Cmd != 0)
      {
          switch (gUART_Cmd)
          {
              case 'w':
            	  WS2812_SetPixel(30, 0, 0);
            	  TB6612_Drive(&gMotorA, 30);
            	  TB6612_Drive(&gMotorB, 30);
            	  break;

              case 's':
            	  WS2812_SetPixel(0, 30, 0);
            	  TB6612_Drive(&gMotorA, -30);
            	  TB6612_Drive(&gMotorB, -30);
            	  break;

              case 'a':
            	  WS2812_SetPixel(0, 0, 30);
            	  TB6612_Drive(&gMotorA, 30);
            	  TB6612_Brake(&gMotorB);
            	  break;

              case 'd':
            	  WS2812_SetPixel(30, 30, 0);
            	  TB6612_Brake(&gMotorA);
            	  TB6612_Drive(&gMotorB, 30);
            	  break;

              case 'x':
            	  WS2812_SetPixel(0, 0, 0);
            	  TB6612_Brake(&gMotorA);
            	  TB6612_Brake(&gMotorB);
            	  break;
          }
          gUART_Cmd = 0;
      }

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

